package com.neon.domain;

public class Output {

	private Long id;
	private String item;
	private double money;
	private int month;
	private int year;
	public Long getId() {
		return id;
	}
	public String getItem() {
		return item;
	}
	public double getMoney() {
		return money;
	}
	public int getMonth() {
		return month;
	}
	public int getYear() {
		return year;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public void setMoney(double money) {
		this.money = money;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public void setYear(int year) {
		this.year = year;
	}
	
	
}
